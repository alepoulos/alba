	public function delete($con = null)
	{
		if ($this->isDeleted()) {
			throw new PropelException("This object has already been deleted.");
		}

		if ($con === null) {
			$con = Propel::getConnection(TipodocentePeer::DATABASE_NAME);
		}

		try {
			$con->begin();
			TipodocentePeer::doDelete($this, $con);
